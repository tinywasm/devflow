// TestUpdateBadges_ViaGotest simulates the full gotest flow calling UpdateBadges on a README
// with the old verbose format. This is the exact scenario that caused 4 lines in the README.
func TestUpdateBadges_ViaGotest(t *testing.T) {
	tmp := t.TempDir()
	readmeFile := filepath.Join(tmp, "README.md")

	// This is the exact old format found in repos like tinywasm/mcp
	oldFormat := `# My Module
<!-- START_SECTION:BADGES_SECTION -->
<a href="docs/img/badges.svg"><img src="docs/img/badges.svg" alt="Project Badges" title="Generated by devflow from github.com/tinywasm/devflow"></a>
<!-- END_SECTION:BADGES_SECTION -->

Some description here.
`
	if err := os.WriteFile(readmeFile, []byte(oldFormat), 0o644); err != nil {
		t.Fatalf("setup: %v", err)
	}

	// Simulate what gotest does: creates NewBadges() with no args and calls updateBadges
	bh := devflow.NewBadges("dummy") // needs at least one arg to not error on init
	if bh.Err() != nil {
		t.Fatalf("NewBadges error: %v", bh.Err())
	}

	err := bh.UpdateBadges(readmeFile, "MIT", "1.24", "Passing", "90", "Clean", "OK", true)
	if err != nil {
		t.Fatalf("UpdateBadges error: %v", err)
	}

	content, err := os.ReadFile(readmeFile)
	if err != nil {
		t.Fatalf("read readme: %v", err)
	}
	got := string(content)

	// Exactly ONE img line
	count := strings.Count(got, `<img src="`)
	if count != 1 {
		t.Errorf("expected 1 badge img line, got %d\nContent:\n%s", count, got)
	}

	// No old markers
	if strings.Contains(got, "START_SECTION") || strings.Contains(got, "END_SECTION") {
		t.Errorf("old markers still present\nContent:\n%s", got)
	}

	// No <a> wrapper
	if strings.Contains(got, "<a href=") {
		t.Errorf("<a> wrapper still present\nContent:\n%s", got)
	}

	// Second call (idempotent)
	bh2 := devflow.NewBadges("dummy")
	if err := bh2.UpdateBadges(readmeFile, "MIT", "1.24", "Passing", "90", "Clean", "OK", true); err != nil {
		t.Fatalf("second UpdateBadges error: %v", err)
	}

	content2, _ := os.ReadFile(readmeFile)
	if strings.Count(string(content2), `<img src="`) != 1 {
		t.Errorf("after second call: duplicate badge lines\nContent:\n%s", string(content2))
	}
}

// TestBuildBadges_NoDoubleInsertion verifies old format with <a> tags doesn't cause duplication
// This tests the exact issue found in tinywasm/mcp README where running badges twice
// on the old format results in duplicate img lines
func TestBuildBadges_NoDoubleInsertion(t *testing.T) {
	tmp := t.TempDir()
	svgOut := filepath.Join(tmp, "badges.svg")
	readmeFile := filepath.Join(tmp, "README.md")

	// Create a README with OLD verbose format (with <a> wrapper)
	oldFormat := `# My Project
<!-- START_SECTION:BADGES_SECTION -->
<a href="docs/img/badges.svg"><img src="docs/img/badges.svg" alt="Project Badges" title="Generated by devflow from github.com/tinywasm/devflow"></a>
<!-- END_SECTION:BADGES_SECTION -->

Some description here.
`
	if err := os.WriteFile(readmeFile, []byte(oldFormat), 0o644); err != nil {
		t.Fatalf("setup: write readme: %v", err)
	}

	// First run: migrate old format
	h1 := devflow.NewBadges("output_svgfile:"+svgOut, "readmefile:"+readmeFile, "License:MIT:#007acc")
	if h1 == nil {
		t.Fatal("NewBadges returned nil")
	}

	_, err := h1.BuildBadges()
	if err != nil {
		t.Fatalf("first BuildBadges error: %v", err)
	}

	content1, err := os.ReadFile(readmeFile)
	if err != nil {
		t.Fatalf("read after first run: %v", err)
	}
	contentStr1 := string(content1)

	// Verify first run result: should have exactly ONE badge line
	badgeLineCount := strings.Count(contentStr1, `<img src="`)
	if badgeLineCount != 1 {
		t.Errorf("After first run: expected 1 badge img line, got %d\nContent:\n%s", badgeLineCount, contentStr1)
	}

	// Verify no old markers remain
	if strings.Contains(contentStr1, "START_SECTION") || strings.Contains(contentStr1, "END_SECTION") {
		t.Errorf("After first run: old markers still present\nContent:\n%s", contentStr1)
	}

	// Verify no <a> tag wrapper
	if strings.Contains(contentStr1, "<a href=") {
		t.Errorf("After first run: old <a> wrapper still present\nContent:\n%s", contentStr1)
	}

	// Second run: update should be idempotent
	h2 := devflow.NewBadges("output_svgfile:"+svgOut, "readmefile:"+readmeFile, "License:MIT:#007acc")
	if h2 == nil {
		t.Fatal("NewBadges returned nil")
	}

	_, err = h2.BuildBadges()
	if err != nil {
		t.Fatalf("second BuildBadges error: %v", err)
	}

	content2, err := os.ReadFile(readmeFile)
	if err != nil {
		t.Fatalf("read after second run: %v", err)
	}
	contentStr2 := string(content2)

	// Verify second run didn't duplicate: still exactly ONE badge line
	badgeLineCount2 := strings.Count(contentStr2, `<img src="`)
	if badgeLineCount2 != 1 {
		t.Errorf("After second run: expected 1 badge img line, got %d\nContent:\n%s", badgeLineCount2, contentStr2)
	}

	// Content should be identical after second run (idempotent)
	if contentStr1 != contentStr2 {
		t.Errorf("Second run changed content (not idempotent)\nFirst:\n%s\n\nSecond:\n%s", contentStr1, contentStr2)
	}
}
