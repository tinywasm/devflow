func TestGenerate_MatchesExpected(t *testing.T) {
	// args designed to produce the expected.svg in testdata
	args := []string{
		"License:MIT:#007acc",
		"Go:1.24.4:#00ADD8",
		"Tests:Passing:#4c1",
		"Coverage:73%:#dfb317",
		"Race:Clean:#4c1",
		"Vet:OK:#4c1",
	}

	h := devflow.NewBadges(args...)
	if h == nil {
		t.Fatalf("NewBadges returned nil")
	}
	if err := h.Err(); err != nil {
		t.Fatalf("NewBadges error: %v", err)
	}

	svgBytes, _, err := h.GenerateSVG()
	if err != nil {
		t.Fatalf("GenerateSVG error: %v", err)
	}
	svg := string(svgBytes)

	wantPath := filepath.Join("testdata", "expected.svg")
	wantBytes, rerr := os.ReadFile(wantPath)
	if rerr != nil {
		t.Fatalf("read expected file: %v", rerr)
	}

	got := strings.TrimSpace(svg)
	want := strings.TrimSpace(string(wantBytes))

	if got != want {
		t.Fatalf("generated SVG differs from expected\n--- got ---\n%s\n--- want ---\n%s", got, want)
	}
}

// TestBuildBadges_UpdatesReadme verifies that BuildBadges updates the README with the new badge format
func TestBuildBadges_UpdatesReadme(t *testing.T) {
	tmp := t.TempDir()
	svgOut := filepath.Join(tmp, "badges.svg")
	readmeFile := filepath.Join(tmp, "README.md")

	// Create a README with some initial content
	initialContent := "# My Project\n\nSome text here.\n"
	if err := os.WriteFile(readmeFile, []byte(initialContent), 0o644); err != nil {
		t.Fatalf("setup: write readme: %v", err)
	}

	// Create handler with explicit output and readme file settings
	h := devflow.NewBadges("output_svgfile:"+svgOut, "readmefile:"+readmeFile, "License:MIT:#007acc")
	if h == nil {
		t.Fatal("NewBadges returned nil")
	}
	if err := h.Err(); err != nil {
		t.Fatalf("handler init error: %v", err)
	}

	if _, err := h.BuildBadges(); err != nil {
		t.Fatalf("BuildBadges error: %v", err)
	}

	// Verify README was updated with badge line
	readmeContent, err := os.ReadFile(readmeFile)
	if err != nil {
		t.Fatalf("read updated readme: %v", err)
	}
	readmeStr := string(readmeContent)

	// Should contain the badge img line
	if !strings.Contains(readmeStr, `<img src=`) {
		t.Errorf("README missing badge img line. Content:\n%s", readmeStr)
	}

	// Should NOT contain old section markers
	if strings.Contains(readmeStr, "START_SECTION") || strings.Contains(readmeStr, "END_SECTION") {
		t.Errorf("README contains old section markers. Content:\n%s", readmeStr)
	}

	// Verify SVG file was written
	info, err := os.Stat(svgOut)
	if err != nil {
		t.Fatalf("expected output file %s to exist: %v", svgOut, err)
	}
	if info.Size() == 0 {
		t.Fatalf("output file %s is empty", svgOut)
	}
}

// TestBuildBadges_MigrateOldMarkers verifies old START/END markers are removed
